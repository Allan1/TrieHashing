#include <stdio.h>

int main(int argc, char **argv){
	int op;
	
	scanf("%c",&op);
	while (op != 'e') {
		switch (op) {
			case 'i':
				break;
			case 't':
				break;
			case 'a':
				break;
			case 'r':
				break;
			case 'f':
				break;
			case 'o':
				break;
			case 'p':
				break;
			case 'd':
				break;
			case 'e':
				return 0;
				break;
			default:
				break;
		}
		scanf("%c",&op);
	}
	
	return 0;
}

